import UIKit.UIFont

public extension UIFont {

    static func neurimboGothicRelgular(size: CGFloat) -> UIFont {
        return UIFont(name: "neurimboGothicRegular", size: size)!
    }

    static func pretendardBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Pretendard-Bold", size: size)!
    }

    static func pretendardMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "Pretendard-Medium", size: size)!
    }

    static func pretendardRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Pretendard-Regular", size: size)!
    }
}
